package com.github.herau.domain;

/**
 * Representation of the four cardinal directions: north, east, south, and west, commonly denoted by their initials: N, E, S, W
 */
public enum Cardinal {
    /**
     * North
     */
    N,
    /**
     * East
     */
    E,
    /**
     * South
     */
    S,
    /**
     * West
     */
    W
}
